package com.cnergee.mypage.obj;

public class ComplaintObj {
	
	String MemberComplaintNo;
	String MemberId;
	String Message;
	String ComplaintId;
	public String getMemberComplaintNo() {
		return MemberComplaintNo;
	}
	public void setMemberComplaintNo(String memberComplaintNo) {
		MemberComplaintNo = memberComplaintNo;
	}
	public String getMemberId() {
		return MemberId;
	}
	public void setMemberId(String memberId) {
		MemberId = memberId;
	}
	public String getMessage() {
		return Message;
	}
	public void setMessage(String message) {
		Message = message;
	}
	public String getComplaintId() {
		return ComplaintId;
	}
	public void setComplaintId(String complaintId) {
		ComplaintId = complaintId;
	}
	
	

}
